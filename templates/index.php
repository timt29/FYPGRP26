            staff were immediately deployed to rectify the fault. He said the
            affected train was moved towards the overrun tracks to clear the
            line, and train services are resuming. "We apologise for the
            disruption during the morning peak commute and sincerely
            appreciate your patience and understanding," Lam said. Previous
            disruptions This is the latest incident in a recent series of
            train disruptions across Singapore's MRT and LRT networks. Most
            recently, a signalling fault on the Downtown Line, which is
